    public void Insertar(){
        Connection conn=null;
        con=new Conectar();
        try {
            conn=con.getConnection();
            PreparedStatement ps = conn.prepareStatement("insert into usertable(username,email,password,celular,direccion)values(?,?,?,?,?)");
            ps.setString(1, txtusername.getText());
            ps.setString(2, txtemail.getText());
            ps.setString(3, txtpassword.getText());
            ps.setInt(4, parseInt(txtcelular.getText()));
            ps.setString(5, txtdireccion.getText());
            int res=ps.executeUpdate();
            if(res>0){
                System.out.println("se registro");
            }else{
                System.out.println("dato no registrado");
            }
            conn.close();
        } catch (Exception e) {
            System.out.println("error");
        }
        
        
                
    }
